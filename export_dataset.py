"""
Export the videos used during training/validation to an organized folder.

Usage (in notebook or standalone):
    from export_dataset import export_used_videos
    export_used_videos(
        split_cache_path=SPLIT_CACHE,
        output_dir="outputs/dataset",
        copy_videos=True,   # set False to only save CSVs (no file copying)
    )
"""

import csv
import json
import os
import shutil

from tqdm import tqdm


def export_used_videos(split_cache_path, output_dir, copy_videos=True):
    """
    Export train/val video lists (and optionally the video files) to output_dir.

    Folder structure created:
        output_dir/
          train/
            real/   ← video files (if copy_videos=True)
            fake/
          val/
            real/
            fake/
          train.csv ← path, source, seg_start, seg_end
          val.csv

    Parameters
    ----------
    split_cache_path : str
        Path to split_cache.json generated by get_train_val_loaders.
    output_dir : str
        Destination folder. Created if it doesn't exist.
    copy_videos : bool
        If True, copy video files to the folder structure.
        If False, only save the CSV manifests (fast, no disk space used).
    """
    if not os.path.exists(split_cache_path):
        raise FileNotFoundError(
            f"Split cache not found: {split_cache_path}\n"
            "Run get_train_val_loaders first to generate it."
        )

    print(f"Loading split cache from: {split_cache_path}")
    with open(split_cache_path, "r") as f:
        cached = json.load(f)

    train_files = [tuple(e) for e in cached["train"]]
    val_files   = [tuple(e) for e in cached["val"]]
    print(f"  Train: {len(train_files)} entries | Val: {len(val_files)} entries")

    # Create folder structure
    for split in ("train", "val"):
        for cls in ("real", "fake", "deepfake"):
            os.makedirs(os.path.join(output_dir, split, cls), exist_ok=True)

    INT_TO_CLASS = {0: "real", 1: "fake", 2: "deepfake"}

    def _class_str(label):
        return INT_TO_CLASS.get(int(label), "fake")

    # Save CSVs + optionally copy videos
    for split_name, files in [("train", train_files), ("val", val_files)]:
        csv_path = os.path.join(output_dir, f"{split_name}.csv")
        rows = []

        for entry in tqdm(files, desc=f"{'Copying' if copy_videos else 'Scanning'} {split_name}"):
            src_path = entry[0]
            label    = entry[1]
            source   = entry[2] if len(entry) > 2 else "unknown"
            seg_start = entry[3] if len(entry) > 3 else ""
            seg_end   = entry[4] if len(entry) == 5 else ""

            cls = _class_str(label)
            filename = os.path.basename(src_path)

            if copy_videos:
                dst_path = os.path.join(output_dir, split_name, cls, filename)
                # Avoid overwriting if duplicate filename across sources
                if os.path.exists(dst_path):
                    name, ext = os.path.splitext(filename)
                    dst_path = os.path.join(
                        output_dir, split_name, cls, f"{source}_{name}{ext}"
                    )
                shutil.copy2(src_path, dst_path)
                rel_path = os.path.relpath(dst_path, output_dir)
            else:
                rel_path = src_path

            rows.append([rel_path, cls, source, seg_start, seg_end])

        with open(csv_path, "w", newline="") as f:
            writer = csv.writer(f)
            writer.writerow(["path", "class", "source", "seg_start", "seg_end"])
            writer.writerows(rows)

        print(f"  {split_name}.csv saved: {csv_path} ({len(rows)} rows)")

    print(f"\nDone. Output: {output_dir}")
    if not copy_videos:
        print("(copy_videos=False — only CSVs saved, no files copied)")
